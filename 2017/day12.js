function formGroup(index) {
	const group = [index];

	function expandGroup(group, member) {
		const connections = list[member];
		const newMembers = _.filter(connections, con => {
			return !_.contains(group, con);
		});
		if (!newMembers.length) {
			return group;
		}
		group = group.concat(newMembers);
		_.each(newMembers, newbie => {
			group = expandGroup(group, newbie);
		});
		return group;
	}

	return expandGroup(group, index);
}

