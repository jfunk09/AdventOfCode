function getWeight(nodeName) {
	var node = input[indexMap[nodeName]];
	// Leaf node
	if (!node.children) {
		return node.weight;
	}

	var children = _.map(node.children, function (childName) {
		return { name: childName, weight: getWeight(childName) };
	});

	if (children.length >= 3 && !unbalanced) {
		checkWeights(children, node);
	}

	var childWeight = _.reduce(children, function (memo, child) {
		return memo + child.weight;
	}, 0);
	return node.weight + childWeight;
}

function checkWeights(children, parent) {
	var weightCheck = children[0].weight === children[1].weight ? children[0].weight :
		children[0].weight === children[2].weight ? children[0].weight : children[1].weight;
	_.each(children, function (child) {
		if (child.weight !== weightCheck && !unbalanced) {
			unbalanced = _.extend({}, parent, { children: children });
			wrongNode = input[indexMap[child.name]];
			var weightMod = weightCheck - child.weight;
			correctedWeight = wrongNode.weight + weightMod;
		}
	});
}
